export function buildSystemPrompt(opts: {
  mode: WorkflowMode;
  template?: BusinessTemplate;
  projectBrief?: string;
  brandVoice?: string;
  brandName?: string;
  brandColors?: string[];
}) {
  const parts: string[] = [BASE_PERSONA];

  if (opts.template && opts.template.id !== "general") {
    parts.push(`\n## سياقُ المشروع\n${opts.template.systemContext}`);
  }

  if (opts.brandName || opts.brandVoice || (opts.brandColors && opts.brandColors.length > 0)) {
    parts.push(`\n## 🎨 ملفُّ العلامة التجارية (التزم به في كلِّ مخرجٍ بصري)`);
    if (opts.brandName) parts.push(`- **اسمُ العلامة:** ${opts.brandName}`);
    if (opts.brandVoice) parts.push(`- **نبرةُ العلامة:** ${opts.brandVoice}`);
    if (opts.brandColors && opts.brandColors.length > 0) {
      parts.push(`- **ألوانُ العلامة الرسمية:** ${opts.brandColors.join(", ")}`);
      parts.push(`  ⚠️ **استخدم هذه الألوان بالضبط** في كلِّ برومبتات التصميم والفيديو (في الخلفيات، العناصر، الإضاءة، الخطوط). لا تستخدم ألواناً أخرى إلا إن طلب العميل صراحةً.`);
    }
  }

  if (opts.projectBrief) {
    parts.push(`\n## ⚠️ وصفُ العميل للمشروع (مرجعٌ أساسيٌّ)\n${opts.projectBrief}\n\n**تذكير:** أيُّ توصيةٍ أو خطةٍ أو محتوًى تقدّمه يجب أن يكون مبنياً على هذا الوصف.`);
  }

  parts.push(MODE_INSTRUCTIONS[opts.mode]);

  return parts.join("\n");
}

